void	ft_swap(int *a, int *b);

//////////////////////////////////////////////////////////////////////////////
int	main(void)
{
	int	c;
	int	d;
	int	*a;
	int	*b;

	c = 225;
	d = 42;
	a = &c;
	b = &d;
	ft_swap(a, b);
}
