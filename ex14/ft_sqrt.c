//////////////////////////////////////////////////////////////////////////////

int	ft_sqrt(int nb)
{
	int	half;
	int	j;

	half = nb / 2;
	j = 1;
	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	while (j <= half)
	{
		if (j * j == nb)
			return (j);
		j++;
	}
	return (0);
}
